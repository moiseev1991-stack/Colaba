/** Найти регион по названию города */
export function findRegionByCity(cityName: string): Region | undefined {
  return REGIONS.find((r) => r.cities.includes(cityName));
}

/** Получить Яндекс-ID региона для города (213 = Москва по умолчанию) */
export function getYandexRegionId(cityName: string): number {
  return findRegionByCity(cityName)?.yandexId ?? 213;
}

/** Плоский список всех городов с привязкой к региону (для combobox-поиска) */
export interface CityOption {
  city: string;
  regionId: string;
  regionName: string;
  yandexId: number;
}

export function getAllCities(): CityOption[] {
  return REGIONS.flatMap((r) =>
    r.cities.map((city) => ({
      city,
      regionId: r.id,
      regionName: r.name,
      yandexId: r.yandexId,
    }))
  );
}

