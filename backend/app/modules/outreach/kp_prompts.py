# Словарь «вхождение → что это значит», для подстановки в промпт КП по
# найденному сайту. Ключи — подстроки (lower-case); матч по `in`.
# Подобран под 4 пресет-чипа из ТЗ Эпика F:
#   «© 2021» / Joomla / «доставка по телефону» / «страница в разработке»
# Юзер может вводить свободные запросы — для них ENTRY_MEANINGS пуст,
# мы не подкидываем LLM натянутую интерпретацию.
ENTRY_MEANINGS: dict[str, str] = {
    "© 2021": "копирайт 2021 года — сайт давно не обновлялся, заброшен",
    "© 2020": "копирайт 2020 года — сайт давно не обновлялся, заброшен",
    "© 2019": "копирайт 2019 года — сайт давно не обновлялся, заброшен",
    "joomla": "сайт на старой CMS Joomla — типичный кандидат на миграцию",
    "drupal 7": "сайт на устаревшей версии Drupal — нужна миграция",
    "wordpress 4": "сайт на устаревшей версии WordPress — нужно обновление",
    "доставка по телефону": (
        "доставка только по телефону — нет интернет-магазина и онлайн-заказа"
    ),
    "заказ по телефону": (
        "заказ только по телефону — нет интернет-магазина и онлайн-заказа"
    ),
    "страница в разработке": (
        "сайт в разработке/незаполнен — компания живая, но веб-фронт не готов"
    ),
    "сайт в разработке": (
        "сайт в разработке/незаполнен — компания живая, но веб-фронт не готов"
    ),
}


def lookup_entry_meaning(entry: str | None) -> str | None:
    """Найти подходящую трактовку для вхождения. Матч по подстроке
    (lower-case). Возвращает None если ничего не найдено — промпт-сборщик
    тогда пропустит строку с признаком, чтобы LLM не выдумывал значение.
    """
    if not entry:
        return None
    low = entry.lower().strip()
    if not low:
        return None
    for key, meaning in ENTRY_MEANINGS.items():
        if key in low:
            return meaning
    return None

