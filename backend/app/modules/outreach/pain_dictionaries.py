"""Справочники «боль → последствие / решение» для каркаса КП «4 хода»
(ТЗ 2026-07-11).

Каркас: LLM получает не «напиши письмо», а заполненный шаблон:
  ХОД1 — доказательство «я смотрел вас»: боль + счётчик + цитата
  ХОД2 — перевод боли в деньги: consequence из справочника
  ХОД3 — решение результатом (не технологией): solution из справочника
  ХОД4 — микрошаг + один вопрос: my_offer_step

Здесь только (1) и (2) — data-driven, LLM их не выдумывает.

По ТЗ структура расширяемая под несколько тем оффера. На старте
единственная — 'automation' (автоматизация связи: боты, онлайн-запись,
обзвон). Добавить другие темы (websites, serm) — заводим ещё словарь
и меняем тему через offer_theme у KpTemplate.

СЕМАНТИКА:
  offer_theme -> {consequence_map, solution_map}
    consequence_map: pain_key -> «фраза-последствие» (для ХОД2)
    solution_map:    pain_key -> «фраза-результат»   (для ХОД3, БЕЗ технологий)

  pain_key — нормализованное имя боли. Мы НЕ полагаемся на конкретные
  PainTag.id (recluster пересобирает id раз в неделю). Вместо этого
  матчим PainTag.label -> pain_key через простую эвристику (см.
  match_pain_key). Если ничего не сматчилось — LLM получит пустые
  ХОД2/ХОД3, промпт-сборщик тогда деградирует до старого поведения.
"""

from __future__ import annotations

import re
from dataclasses import dataclass
from typing import Iterable

# --- Ключи болей, поверх которых строим справочники -------------------------

# Формат key — короткий stable slug. Порядок влияет только на порядок
# перебора в match_pain_key: более конкретные ключи (по длине паттернов)
# идут первыми, чтобы 'call_no_answer' сматчилось раньше 'schedule'.

PAIN_KEYS: tuple[str, ...] = (
    "call_no_answer",     # не дозвониться, не берут трубку, долго ждали ответа
    "callback_lost",      # не перезвонили, потеряли заявку
    "schedule_hard",      # сложно записаться, проблемы с записью
    "schedule_wait",      # долгое ожидание приёма, задержки записи
    "queue_wait",         # очереди, ожидание обслуживания
    "admin_rude",         # грубость администратора, менеджера
    "food_slow",          # долго ждали еду, заказ (общепит)
    "unclear_pricing",    # спорные / необоснованные доплаты за приём/услугу
)


# --- Матчер PainTag.label -> pain_key ---------------------------------------

# Паттерны для матчинга. Каждый key -> список ГРУПП. Группа = список
# подстрок, ВСЕ из которых должны быть в label (case-insensitive) —
# так мы устойчивы к падежам («грубость администратора» матчится
# группой ["груб", "администрат"], хотя ни «груб администрат» ни
# «грубости админ» не были бы continuous-подстрокой).
# Если хотя бы одна группа сматчилась — key определён.
_PAIN_KEY_PATTERNS: dict[str, tuple[tuple[str, ...], ...]] = {
    "call_no_answer": (
        ("дозвон",),
        ("не берут",),
        ("не отвеча", "звон"),
        ("не берёт", "трубк"),
        ("не берет", "трубк"),
        ("долго", "ждали", "ответ"),
    ),
    "callback_lost": (
        ("перезвон",),
        ("потеря", "заяв"),
        ("потерян", "заяв"),
        ("оставил", "заявк"),
    ),
    "schedule_hard": (
        ("проблем", "запис"),
        ("сложно", "запис"),
        ("неудобн", "запис"),
        ("запис", "прием"),
        ("запис", "приём"),
        ("запис", "услуг"),
    ),
    "schedule_wait": (
        ("ожидан", "прием"),
        ("ожидан", "приём"),
        ("задержк", "запис"),
        ("перенос", "запис"),
    ),
    "queue_wait": (
        ("очеред",),
        ("ожидан", "обслуж"),
        ("ожидан", "на кассе"),
        ("ожидан", "подключ"),
    ),
    "admin_rude": (
        ("груб", "администрат"),
        ("хам", "администрат"),
        ("неприветлив", "администрат"),
        ("непрофес", "администрат"),
        ("груб", "менеджер"),
        ("хам", "менеджер"),
    ),
    "food_slow": (
        ("ожидан", "еды"),
        ("ожидан", "заказ"),
        ("ожидан", "обед"),
    ),
    "unclear_pricing": (
        ("необоснов", "доплат"),
        ("спорн", "доплат"),
        ("неожидан", "доплат"),
    ),
}


def match_pain_key(label: str) -> str | None:
    """Определяет pain_key по человеко-читаемому label от PainTag.

    Матчинг: label содержит все подстроки хотя бы одной группы паттернов
    для key. Устойчиво к падежам («грубость администратора» = «груб» +
    «администрат»).

    Возвращает None если ни один паттерн не сматчился (тогда промпт-сборщик
    пропустит ХОД2/ХОД3 для этой боли — LLM не будет выдумывать).
    """
    if not label:
        return None
    low = label.lower().strip()
    if not low:
        return None
    for key in PAIN_KEYS:
        for group in _PAIN_KEY_PATTERNS.get(key, ()):
            if all(p in low for p in group):
                return key
    return None


