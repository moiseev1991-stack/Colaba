def get_search_queries(niche: str) -> list[str]:
    """Возвращает список реальных поисковых запросов для multi-query expansion.

    Если ниша есть в словаре синонимов — возвращает 2-4 запроса.
    Если нет — возвращает [niche] — один запрос как обычно (backwards-compat).
    Пустая ниша → пустой список (caller проверит и не запустит парсер).
    """
    if not niche:
        return []
    key = niche.strip().lower()
    return NICHE_SYNONYMS.get(key) or [niche.strip()]
